    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if (!dishField.getText().equals("")) {
            Number price = (Number) priceField.getValue();
            Number priceDouble;
            try {
                priceDouble = (long) price;
            } catch (Exception e) {
                priceDouble = (double) price;
            }
            String available = (String) availabilityComboBox.getSelectedItem();
            DBConnect database = new DBConnect();
            Statement st = null;
            String insert = "INSERT INTO `crave`.`dishes` (`rid`, `Name`, `Price`, `Available`) VALUES ('" + u.restID + "', '" + dishField.getText() + "', '" + priceDouble + "', '" + available + "');";
            try {
                st = database.getStatement();
                st.executeUpdate(insert);
                dispose();
                rmv.update(u);

            } catch (SQLException ex) {
                dispose();
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

